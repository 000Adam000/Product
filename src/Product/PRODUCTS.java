
package Product;


public class PRODUCTS {
    
    private int ID;
    private String name;
    private int count;
    private int price;
    private String place;
    
    public void setID(int ID){
        this.ID= ID;
    }
    
    public int getID(){
        return ID;
    }
    
    public void setName(String name){
        this.name= name;
    }
    
    public String getName(){
        return name;
    }
    
    public void setCount(int count){
        this.count= count;
    }
    
    public int getCount(){
        return count;
    }
    
    public void setPrice(int price){
        this.price= price;
    }
    
    public int getPrice(){
        return price;
    }
    
    public void setPlace(String place){
        this.place= place;
    }
    
    public String getplace(){
        return place;
    }
    
    
    
    
    
}
